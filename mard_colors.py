def get_color_name(code):
    """获取色号的系列名称"""
    series = {
        'A': '红色系', 'B': '绿色系', 'C': '蓝色系',
        'D': '紫粉系', 'E': '黄橙系', 'F': '肤色系',
        'G': '棕色系', 'H': '黑白灰', 'M': '中间色'
    }
    prefix = code[0]
    return series.get(prefix, '未知')
